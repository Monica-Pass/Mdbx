#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum SyncTransferMode {
    CompleteState,
    PagedCommitInventory,
    IncrementalBundleV4,
}

/// 同步会话状态机。
///
/// 管理双向同步的各个阶段：
///
/// ```text
/// Idle ──► HelloWait ──► Negotiate ──► Exchange ──► Done
///              │              │            │
///              └──────────────┴────────────┘
///                         │
///                       Error
/// ```
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum SyncPhase {
    Idle,
    HelloWait,
    Negotiate,
    Exchange,
    Done,
}

/// 同步协商器。
///
/// 基于双方的 head 信息和已知 commit 集合，
/// 计算出需要发送和接收的 commit 列表。
pub struct SyncNegotiator {
    /// 本地设备 ID。
    device_id: String,

    /// 本方已知的所有 commit ID。
    known_commit_ids: HashSet<String>,

    /// 对方已知的 commit ID（从 Hello 获得）。
    remote_known_commit_ids: HashSet<String>,

    /// 本方与对方声明的可选协议能力。
    local_capabilities: HashSet<String>,
    remote_capabilities: HashSet<String>,

    /// 本地分支 head。
    local_heads: Vec<BranchHead>,

    /// 对方分支 head。
    remote_heads: Vec<BranchHead>,
}

impl SyncNegotiator {
    /// 创建本地协商器。
    pub fn new(
        device_id: &str,
        local_heads: Vec<BranchHead>,
        local_commit_ids: Vec<String>,
    ) -> Self {
        Self {
            device_id: device_id.to_string(),
            known_commit_ids: local_commit_ids.into_iter().collect(),
            remote_known_commit_ids: HashSet::new(),
            local_capabilities: HashSet::new(),
            remote_capabilities: HashSet::new(),
            local_heads,
            remote_heads: Vec::new(),
        }
    }

    /// Create a negotiator that advertises the complete bounded v4 contract.
    /// The Hello generated by [`local_hello`](Self::local_hello) omits the
    /// legacy known-commit vector when the remote peer also supports paging.
    pub fn new_incremental(
        device_id: &str,
        local_heads: Vec<BranchHead>,
        local_commit_ids: Vec<String>,
    ) -> SyncResult<Self> {
        let mut negotiator = Self::new(device_id, local_heads, local_commit_ids);
        negotiator.enable_incremental_capabilities()?;
        Ok(negotiator)
    }

    pub fn enable_incremental_capabilities(&mut self) -> SyncResult<()> {
        for capability in INCREMENTAL_SYNC_CAPABILITIES {
            self.enable_capability(capability)?;
        }
        Ok(())
    }

    /// Advertise the complete optional Blob synchronization contract.
    pub fn enable_blob_replication_capabilities(&mut self) -> SyncResult<()> {
        for capability in BLOB_SYNC_CAPABILITIES {
            self.enable_capability(capability)?;
        }
        Ok(())
    }

    /// Advertise support for v5/v6 zstd bundles when the codec is compiled in.
    pub fn enable_bundle_compression_capability(&mut self) -> SyncResult<()> {
        #[cfg(feature = "zstd-compression")]
        {
            self.enable_capability(CAPABILITY_ZSTD_BUNDLE_V1)
        }
        #[cfg(not(feature = "zstd-compression"))]
        {
            Err(SyncError::UnsupportedFeature(
                "zstd-compression".to_string(),
            ))
        }
    }

    /// Build the local Hello. New peers do not put an unbounded commit-ID
    /// inventory in this message once paging is enabled locally.
    pub fn local_hello(&self) -> SyncResult<HelloRequest> {
        let known_commit_ids = if self
            .local_capabilities
            .contains(CAPABILITY_COMMIT_INVENTORY_PAGING_V1)
        {
            Vec::new()
        } else {
            self.known_commit_ids.iter().cloned().collect()
        };
        HelloRequest::new(&self.device_id, self.local_heads.clone(), known_commit_ids)
            .with_capabilities(self.local_capabilities.iter().cloned().collect())
    }

    /// 处理对方的 Hello 消息并生成己方的 Hello 响应。
    pub fn on_hello(&mut self, hello: &HelloRequest) -> SyncResult<HelloResponse> {
        if hello.protocol_version != PROTOCOL_VERSION {
            return Err(SyncError::VersionMismatch {
                local: PROTOCOL_VERSION,
                remote: hello.protocol_version,
            });
        }
        validate_capabilities(&hello.capabilities)?;

        self.remote_heads = hello.heads.clone();
        self.remote_known_commit_ids = hello.known_commit_ids.iter().cloned().collect();

        self.remote_capabilities = hello.capabilities.iter().cloned().collect();
        let negotiated = self
            .local_capabilities
            .intersection(&self.remote_capabilities)
            .cloned()
            .collect::<Vec<_>>();

        let known_commit_ids =
            if self.capability_is_negotiated(CAPABILITY_COMMIT_INVENTORY_PAGING_V1) {
                Vec::new()
            } else {
                self.known_commit_ids.iter().cloned().collect()
            };
        HelloResponse::new(&self.device_id, self.local_heads.clone(), known_commit_ids)
            .with_capabilities(negotiated)
    }

    /// 处理对方的 Hello 响应。
    pub fn on_hello_ack(&mut self, response: &HelloResponse) -> SyncResult<()> {
        if response.protocol_version != PROTOCOL_VERSION {
            return Err(SyncError::VersionMismatch {
                local: PROTOCOL_VERSION,
                remote: response.protocol_version,
            });
        }
        validate_capabilities(&response.capabilities)?;

        self.remote_heads = response.heads.clone();
        self.remote_known_commit_ids = response.known_commit_ids.iter().cloned().collect();
        self.remote_capabilities = response.capabilities.iter().cloned().collect();
        Ok(())
    }

    /// 计算我方需要从对方拉取的 commit ID。
    ///
    /// 即：对方有而我们没有的 commit。
    pub fn compute_want(&self, remote_commit_ids: &[String]) -> Vec<String> {
        remote_commit_ids
            .iter()
            .filter(|id| !self.known_commit_ids.contains(*id))
            .cloned()
            .collect()
    }

    /// 计算我方需要推送给对方的 commit ID。
    ///
    /// 即：我们有而对方没有的 commit。
    pub fn compute_push(&self, our_extra_ids: &[String]) -> Vec<String> {
        our_extra_ids
            .iter()
            .filter(|id| !self.remote_known_commit_ids.contains(*id))
            .cloned()
            .collect()
    }

    /// 检查双方 head 是否已经一致（无需同步）。
    pub fn is_already_synced(&self) -> bool {
        if self.local_heads.len() != self.remote_heads.len() {
            return false;
        }

        for local in &self.local_heads {
            let remote_match = self.remote_heads.iter().any(|r| {
                same_branch_identity(r, local) && r.head_commit_id == local.head_commit_id
            });
            if !remote_match {
                return false;
            }
        }
        true
    }

    /// 找出对方领先于我们的分支。
    pub fn ahead_branches(&self) -> Vec<&BranchHead> {
        self.remote_heads
            .iter()
            .filter(|remote| {
                !self.local_heads.iter().any(|local| {
                    same_branch_identity(local, remote)
                        && local.head_commit_id == remote.head_commit_id
                })
            })
            .collect()
    }

    // -- accessors --

    pub fn device_id(&self) -> &str {
        &self.device_id
    }

    pub fn local_heads(&self) -> &[BranchHead] {
        &self.local_heads
    }

    pub fn remote_heads(&self) -> &[BranchHead] {
        &self.remote_heads
    }

    pub fn known_commit_ids(&self) -> &HashSet<String> {
        &self.known_commit_ids
    }

    pub fn remote_known_commit_ids(&self) -> &HashSet<String> {
        &self.remote_known_commit_ids
    }

    pub fn enable_capability(&mut self, capability: &str) -> SyncResult<()> {
        let candidate = vec![capability.to_string()];
        validate_capabilities(&candidate)?;
        if self.local_capabilities.len() >= MAX_SYNC_CAPABILITIES
            && !self.local_capabilities.contains(capability)
        {
            return Err(SyncError::InvalidMessage(format!(
                "sync capability list exceeds {MAX_SYNC_CAPABILITIES} items"
            )));
        }
        self.local_capabilities.insert(capability.to_string());
        Ok(())
    }

    pub fn local_capabilities(&self) -> &HashSet<String> {
        &self.local_capabilities
    }

    pub fn remote_capabilities(&self) -> &HashSet<String> {
        &self.remote_capabilities
    }

    pub fn capability_is_negotiated(&self, capability: &str) -> bool {
        self.local_capabilities.contains(capability)
            && self.remote_capabilities.contains(capability)
    }

    /// Blob replication is enabled only when both peers negotiated every
    /// capability needed for bounded, resumable transfer.
    pub fn blob_replication_is_negotiated(&self) -> bool {
        BLOB_SYNC_CAPABILITIES
            .iter()
            .all(|capability| self.capability_is_negotiated(capability))
    }

    /// Compression is usable only when the codec is present and both peers
    /// advertised the independent zstd bundle capability.
    pub fn bundle_compression_is_negotiated(&self) -> bool {
        cfg!(feature = "zstd-compression")
            && self.capability_is_negotiated(CAPABILITY_ZSTD_BUNDLE_V1)
    }

    pub fn negotiated_bundle_compression(&self) -> BundleCompression {
        if self.bundle_compression_is_negotiated() {
            BundleCompression::Zstd
        } else {
            BundleCompression::None
        }
    }

    pub fn transfer_mode(&self) -> SyncTransferMode {
        if INCREMENTAL_SYNC_CAPABILITIES
            .iter()
            .all(|capability| self.capability_is_negotiated(capability))
        {
            SyncTransferMode::IncrementalBundleV4
        } else if self.capability_is_negotiated(CAPABILITY_COMMIT_INVENTORY_PAGING_V1) {
            SyncTransferMode::PagedCommitInventory
        } else {
            SyncTransferMode::CompleteState
        }
    }
}

#[derive(Debug, Clone, PartialEq, Eq)]
pub enum SyncClientPhase {
    Idle,
    HelloSent,
    Negotiated,
    AwaitingSegmentAcknowledgement,
    Complete,
    Fallback,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum BlobSyncPhase {
    Disabled,
    Idle,
    Manifest,
    AwaitingManifestAcknowledgement,
    Chunk,
    AwaitingChunkAcknowledgement,
    Complete,
}

/// Durable Blob progress. Pending requests and validated responses are
/// intentionally excluded so a restart resumes from the last storage ack.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
#[serde(deny_unknown_fields)]
pub struct BlobSyncResume {
    pub namespace_id: String,
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub manifest_checkpoint: Option<String>,
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub manifest_cursor: Option<String>,
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub current_blob_id: Option<String>,
    pub total_size: u64,
    pub next_durable_offset: u64,
    #[serde(default)]
    pub manifest_complete: bool,
}

impl BlobSyncResume {
    pub fn new(namespace_id: String) -> SyncResult<Self> {
        let resume = Self {
            namespace_id,
            manifest_checkpoint: None,
            manifest_cursor: None,
            current_blob_id: None,
            total_size: 0,
            next_durable_offset: 0,
            manifest_complete: false,
        };
        resume.validate()?;
        Ok(resume)
    }

    pub fn validate(&self) -> SyncResult<()> {
        BlobManifestPageRequest::new(
            self.namespace_id.clone(),
            self.manifest_checkpoint.clone(),
            self.manifest_cursor.clone(),
            1,
        )?;
        if self.manifest_cursor.is_some() && self.manifest_checkpoint.is_none() {
            return Err(SyncError::InvalidMessage(
                "Blob manifest cursor requires a checkpoint".to_string(),
            ));
        }
        if self.manifest_complete && self.manifest_cursor.is_some() {
            return Err(SyncError::InvalidMessage(
                "completed Blob manifest cannot retain a cursor".to_string(),
            ));
        }
        match self.current_blob_id.as_deref() {
            Some(blob_id) => {
                BlobChunkRequest::new(
                    self.namespace_id.clone(),
                    blob_id.to_string(),
                    self.total_size,
                    self.next_durable_offset,
                    1,
                )?;
                if self.manifest_complete {
                    return Err(SyncError::InvalidMessage(
                        "completed Blob manifest cannot retain an active Blob".to_string(),
                    ));
                }
            }
            None if self.total_size != 0 || self.next_durable_offset != 0 => {
                return Err(SyncError::InvalidMessage(
                    "Blob resume size and offset require an active Blob".to_string(),
                ));
            }
            None => {}
        }
        Ok(())
    }
}

/// Transport-neutral state holder for a negotiated synchronization exchange.
///
/// Storage adapters call [`acknowledge_incremental_segment`](Self::acknowledge_incremental_segment)
/// only after the segment has been durably applied. Merely receiving or
/// validating a bundle never advances the checkpoint.
pub struct SyncClient {
    negotiator: SyncNegotiator,
    phase: SyncClientPhase,
    mode: SyncTransferMode,
    checkpoint: Option<IncrementalBundleCheckpoint>,
    resume: Option<IncrementalBundleResume>,
    blob_phase: BlobSyncPhase,
    blob_resume: Option<BlobSyncResume>,
    pending_blob_manifest_request: Option<BlobManifestPageRequest>,
    pending_blob_manifest_response: Option<BlobManifestPageResponse>,
    pending_blob_chunk_request: Option<BlobChunkRequest>,
    pending_blob_chunk_response: Option<BlobChunkResponse>,
}

impl SyncClient {
    pub fn new(
        negotiator: SyncNegotiator,
        checkpoint: Option<IncrementalBundleCheckpoint>,
        resume: Option<IncrementalBundleResume>,
    ) -> Self {
        Self {
            negotiator,
            phase: SyncClientPhase::Idle,
            mode: SyncTransferMode::CompleteState,
            checkpoint,
            resume,
            blob_phase: BlobSyncPhase::Disabled,
            blob_resume: None,
            pending_blob_manifest_request: None,
            pending_blob_manifest_response: None,
            pending_blob_chunk_request: None,
            pending_blob_chunk_response: None,
        }
    }

    pub fn hello(&mut self) -> SyncResult<HelloRequest> {
        self.phase = SyncClientPhase::HelloSent;
        self.negotiator.local_hello()
    }

    pub fn on_hello(&mut self, hello: &HelloRequest) -> SyncResult<HelloResponse> {
        let response = self.negotiator.on_hello(hello)?;
        self.mode = self.negotiator.transfer_mode();
        self.refresh_blob_phase();
        self.phase = SyncClientPhase::Negotiated;
        Ok(response)
    }

    pub fn on_hello_ack(&mut self, response: &HelloResponse) -> SyncResult<()> {
        self.negotiator.on_hello_ack(response)?;
        self.mode = self.negotiator.transfer_mode();
        self.refresh_blob_phase();
        self.phase = SyncClientPhase::Negotiated;
        Ok(())
    }

    pub fn negotiator(&self) -> &SyncNegotiator {
        &self.negotiator
    }

    pub fn phase(&self) -> &SyncClientPhase {
        &self.phase
    }

    pub fn transfer_mode(&self) -> SyncTransferMode {
        self.mode
    }

    pub fn checkpoint(&self) -> Option<&IncrementalBundleCheckpoint> {
        self.checkpoint.as_ref()
    }

    pub fn resume(&self) -> Option<&IncrementalBundleResume> {
        self.resume.as_ref()
    }

    pub fn blob_sync_phase(&self) -> BlobSyncPhase {
        self.blob_phase
    }

    pub fn blob_resume(&self) -> Option<&BlobSyncResume> {
        self.blob_resume.as_ref()
    }

    pub fn blob_replication_is_negotiated(&self) -> bool {
        self.negotiator.blob_replication_is_negotiated()
    }

    pub fn begin_blob_sync(&mut self, namespace_id: String) -> SyncResult<()> {
        self.restore_blob_sync(BlobSyncResume::new(namespace_id)?)
    }

    pub fn restore_blob_sync(&mut self, resume: BlobSyncResume) -> SyncResult<()> {
        resume.validate()?;
        self.blob_resume = Some(resume);
        self.clear_pending_blob_exchange();
        self.refresh_blob_phase();
        Ok(())
    }

    pub fn blob_manifest_request(
        &mut self,
        page_size: usize,
    ) -> SyncResult<BlobManifestPageRequest> {
        self.require_blob_replication()?;
        let resume = self.blob_resume.as_ref().ok_or_else(|| {
            SyncError::InvalidMessage(
                "Blob synchronization has no configured namespace".to_string(),
            )
        })?;
        if resume.manifest_complete {
            return Err(SyncError::InvalidMessage(
                "Blob manifest synchronization is already complete".to_string(),
            ));
        }
        if resume.current_blob_id.is_some() || self.pending_blob_chunk_request.is_some() {
            return Err(SyncError::InvalidMessage(
                "cannot request a Blob manifest page during an active Blob transfer".to_string(),
            ));
        }
        let request = BlobManifestPageRequest::new(
            resume.namespace_id.clone(),
            resume.manifest_checkpoint.clone(),
            resume.manifest_cursor.clone(),
            page_size,
        )?;
        self.pending_blob_manifest_request = Some(request.clone());
        self.pending_blob_manifest_response = None;
        self.blob_phase = BlobSyncPhase::Manifest;
        Ok(request)
    }

    pub fn validate_blob_manifest_response(
        &mut self,
        response: &BlobManifestPageResponse,
    ) -> SyncResult<()> {
        self.require_blob_replication()?;
        response.validate()?;
        let request = self.pending_blob_manifest_request.as_ref().ok_or_else(|| {
            SyncError::InvalidMessage("Blob manifest response has no pending request".to_string())
        })?;
        if response.namespace_id != request.namespace_id {
            return Err(SyncError::InvalidMessage(
                "Blob manifest namespace does not match the pending request".to_string(),
            ));
        }
        if request
            .checkpoint
            .as_deref()
            .is_some_and(|checkpoint| checkpoint != response.checkpoint)
        {
            return Err(SyncError::InvalidMessage(
                "Blob manifest checkpoint changed during paging".to_string(),
            ));
        }
        self.pending_blob_manifest_response = Some(response.clone());
        self.blob_phase = BlobSyncPhase::AwaitingManifestAcknowledgement;
        Ok(())
    }

    /// Advance manifest progress only after the page has been fully processed
    /// and any required Provider writes are durable.
    pub fn acknowledge_blob_manifest_page(
        &mut self,
        response: &BlobManifestPageResponse,
    ) -> SyncResult<()> {
        if self.pending_blob_manifest_response.as_ref() != Some(response) {
            self.validate_blob_manifest_response(response)?;
        }
        let resume = self.blob_resume.as_mut().ok_or_else(|| {
            SyncError::InvalidMessage(
                "Blob synchronization has no durable resume state".to_string(),
            )
        })?;
        if resume.current_blob_id.is_some() || self.pending_blob_chunk_request.is_some() {
            return Err(SyncError::InvalidMessage(
                "cannot acknowledge a Blob manifest page during an active Blob transfer"
                    .to_string(),
            ));
        }
        resume.manifest_checkpoint = Some(response.checkpoint.clone());
        resume.manifest_cursor = response.next_cursor.clone();
        resume.manifest_complete = response.next_cursor.is_none();
        self.pending_blob_manifest_request = None;
        self.pending_blob_manifest_response = None;
        self.blob_phase = if resume.manifest_complete {
            BlobSyncPhase::Complete
        } else {
            BlobSyncPhase::Manifest
        };
        Ok(())
    }

    pub fn blob_chunk_request(
        &mut self,
        blob_id: String,
        total_size: u64,
        max_bytes: usize,
    ) -> SyncResult<BlobChunkRequest> {
        self.require_blob_replication()?;
        let resume = self.blob_resume.as_ref().ok_or_else(|| {
            SyncError::InvalidMessage(
                "Blob synchronization has no configured namespace".to_string(),
            )
        })?;
        if resume.manifest_complete {
            return Err(SyncError::InvalidMessage(
                "Blob manifest synchronization is already complete".to_string(),
            ));
        }
        let offset = match resume.current_blob_id.as_deref() {
            Some(current) if current == blob_id && resume.total_size == total_size => {
                resume.next_durable_offset
            }
            Some(_) => {
                return Err(SyncError::InvalidMessage(
                    "Blob request does not match the durable active Blob".to_string(),
                ))
            }
            None => {
                let manifest = self
                    .pending_blob_manifest_response
                    .as_ref()
                    .ok_or_else(|| {
                        SyncError::InvalidMessage(
                            "new Blob transfer requires a validated manifest page".to_string(),
                        )
                    })?;
                let declared = manifest.items.iter().any(|item| {
                    item.blob_id == blob_id
                        && item.total_size == Some(total_size)
                        && item.state == BlobManifestEntryState::Available
                });
                if !declared {
                    return Err(SyncError::InvalidMessage(
                        "Blob request is not an available item in the validated manifest page"
                            .to_string(),
                    ));
                }
                0
            }
        };
        let request = BlobChunkRequest::new(
            resume.namespace_id.clone(),
            blob_id,
            total_size,
            offset,
            max_bytes,
        )?;
        self.pending_blob_chunk_request = Some(request.clone());
        self.pending_blob_chunk_response = None;
        self.blob_phase = BlobSyncPhase::Chunk;
        Ok(request)
    }

    pub fn validate_blob_chunk_response(&mut self, response: &BlobChunkResponse) -> SyncResult<()> {
        self.require_blob_replication()?;
        response.validate()?;
        let request = self.pending_blob_chunk_request.as_ref().ok_or_else(|| {
            SyncError::InvalidMessage("Blob chunk response has no pending request".to_string())
        })?;
        if response.namespace_id != request.namespace_id
            || response.blob_id != request.blob_id
            || response.total_size != request.total_size
            || response.offset != request.offset
        {
            return Err(SyncError::InvalidMessage(
                "Blob chunk response does not match the pending request".to_string(),
            ));
        }
        if response.ciphertext.len() > request.max_bytes as usize {
            return Err(SyncError::InvalidMessage(
                "Blob chunk response exceeds the requested byte limit".to_string(),
            ));
        }
        self.pending_blob_chunk_response = Some(response.clone());
        self.blob_phase = BlobSyncPhase::AwaitingChunkAcknowledgement;
        Ok(())
    }

    /// Advance the ciphertext offset only after the Provider confirms that
    /// this exact chunk is durable.
    pub fn acknowledge_blob_chunk(&mut self, response: &BlobChunkResponse) -> SyncResult<()> {
        if self.pending_blob_chunk_response.as_ref() != Some(response) {
            self.validate_blob_chunk_response(response)?;
        }
        let end = response
            .offset
            .checked_add(response.ciphertext.len() as u64)
            .ok_or_else(|| SyncError::InvalidMessage("Blob chunk offset overflow".to_string()))?;
        let resume = self.blob_resume.as_mut().ok_or_else(|| {
            SyncError::InvalidMessage(
                "Blob synchronization has no durable resume state".to_string(),
            )
        })?;
        if response.is_last {
            resume.current_blob_id = None;
            resume.total_size = 0;
            resume.next_durable_offset = 0;
        } else {
            resume.current_blob_id = Some(response.blob_id.clone());
            resume.total_size = response.total_size;
            resume.next_durable_offset = end;
        }
        self.pending_blob_chunk_request = None;
        self.pending_blob_chunk_response = None;
        self.blob_phase = if self.pending_blob_manifest_response.is_some() {
            BlobSyncPhase::AwaitingManifestAcknowledgement
        } else {
            BlobSyncPhase::Chunk
        };
        Ok(())
    }

    /// Reset an active Blob to offset zero after the storage Provider has
    /// durably abandoned its partial bytes. This is the recovery path for a
    /// final content-address verification failure.
    pub fn restart_blob_transfer_after_abort(
        &mut self,
        blob_id: &str,
        total_size: u64,
    ) -> SyncResult<()> {
        self.require_blob_replication()?;
        let resume = self.blob_resume.as_mut().ok_or_else(|| {
            SyncError::InvalidMessage(
                "Blob synchronization has no durable resume state".to_string(),
            )
        })?;
        match resume.current_blob_id.as_deref() {
            Some(current) if current == blob_id && resume.total_size == total_size => {
                resume.next_durable_offset = 0;
            }
            None if resume.total_size == 0 && resume.next_durable_offset == 0 => {}
            _ => {
                return Err(SyncError::InvalidMessage(
                    "aborted Blob does not match the durable transfer state".to_string(),
                ))
            }
        }
        self.pending_blob_chunk_request = None;
        self.pending_blob_chunk_response = None;
        self.blob_phase = if self.pending_blob_manifest_response.is_some() {
            BlobSyncPhase::AwaitingManifestAcknowledgement
        } else {
            BlobSyncPhase::Chunk
        };
        Ok(())
    }

    /// Record a completed complete-state bootstrap. The storage adapter must
    /// call this only after the bootstrap transaction is durable.
    pub fn acknowledge_complete_bootstrap(
        &mut self,
        checkpoint: IncrementalBundleCheckpoint,
    ) -> SyncResult<()> {
        if checkpoint.commit_inventory.is_none() || checkpoint.delta_inventory.is_none() {
            return Err(SyncError::InvalidMessage(
                "bootstrap acknowledgement requires a paired checkpoint".to_string(),
            ));
        }
        self.checkpoint = Some(checkpoint);
        self.resume = None;
        self.phase = SyncClientPhase::Negotiated;
        Ok(())
    }

    pub fn commit_inventory_request(
        &self,
        cursor: Option<String>,
        page_size: usize,
    ) -> SyncResult<CommitInventoryPageRequest> {
        if self.transfer_mode() == SyncTransferMode::CompleteState {
            return Err(SyncError::InvalidMessage(
                "peer did not negotiate commit inventory paging".to_string(),
            ));
        }
        CommitInventoryPageRequest::new(
            self.checkpoint
                .as_ref()
                .and_then(|checkpoint| checkpoint.commit_inventory.clone()),
            cursor,
            page_size,
        )
    }

    pub fn delta_inventory_request(
        &self,
        cursor: Option<String>,
        page_size: usize,
    ) -> SyncResult<DeltaInventoryPageRequest> {
        if self.transfer_mode() != SyncTransferMode::IncrementalBundleV4 {
            return Err(SyncError::InvalidMessage(
                "peer did not negotiate the complete delta inventory contract".to_string(),
            ));
        }
        DeltaInventoryPageRequest::new(
            self.checkpoint
                .as_ref()
                .and_then(|checkpoint| checkpoint.delta_inventory.clone()),
            cursor,
            page_size,
        )
    }

    pub fn validate_incremental_segment(
        &mut self,
        bundle: &IncrementalSyncBundle,
    ) -> SyncResult<()> {
        if self.transfer_mode() != SyncTransferMode::IncrementalBundleV4 {
            return Err(SyncError::InvalidMessage(
                "peer did not negotiate incremental bundle v4".to_string(),
            ));
        }
        let expected_base = self.checkpoint.as_ref().ok_or_else(|| {
            SyncError::InvalidMessage("incremental client has no bootstrap checkpoint".to_string())
        })?;
        bundle.validate()?;
        if &bundle.manifest.base != expected_base {
            return Err(SyncError::InvalidMessage(
                "incremental segment base does not match the client checkpoint".to_string(),
            ));
        }
        match self.resume.as_ref() {
            Some(resume)
                if resume.transfer_id == bundle.manifest.transfer_id
                    && resume.next_segment_index == bundle.manifest.segment_index
                    && bundle.manifest.previous_segment_sha256.as_deref()
                        == Some(resume.previous_segment_sha256.as_slice()) => {}
            Some(_) => {
                return Err(SyncError::InvalidMessage(
                    "incremental segment does not match the saved transfer resume state"
                        .to_string(),
                ))
            }
            None if bundle.manifest.segment_index == 0
                && bundle.manifest.previous_segment_sha256.is_none() => {}
            None => {
                return Err(SyncError::InvalidMessage(
                    "incremental segment requires resume state".to_string(),
                ))
            }
        }
        self.phase = SyncClientPhase::AwaitingSegmentAcknowledgement;
        Ok(())
    }

    /// Advance memory state after the storage adapter reports durable apply.
    pub fn acknowledge_incremental_segment(
        &mut self,
        bundle: &IncrementalSyncBundle,
    ) -> SyncResult<()> {
        self.validate_incremental_segment(bundle)?;
        self.checkpoint = Some(bundle.manifest.result.clone());
        self.resume = if bundle.manifest.is_last {
            self.phase = SyncClientPhase::Complete;
            None
        } else {
            Some(IncrementalBundleResume {
                transfer_id: bundle.manifest.transfer_id.clone(),
                next_segment_index: bundle.manifest.segment_index.checked_add(1).ok_or_else(
                    || SyncError::InvalidMessage("segment index overflow".to_string()),
                )?,
                previous_segment_sha256: incremental_bundle_payload_sha256(bundle)?,
            })
        };
        Ok(())
    }

    pub fn fallback_to_complete_state(&mut self) {
        self.mode = SyncTransferMode::CompleteState;
        self.phase = SyncClientPhase::Fallback;
        self.resume = None;
    }

    fn require_blob_replication(&self) -> SyncResult<()> {
        if !self.blob_replication_is_negotiated() {
            return Err(SyncError::InvalidMessage(
                "peer did not negotiate the complete Blob replication contract".to_string(),
            ));
        }
        Ok(())
    }

    fn clear_pending_blob_exchange(&mut self) {
        self.pending_blob_manifest_request = None;
        self.pending_blob_manifest_response = None;
        self.pending_blob_chunk_request = None;
        self.pending_blob_chunk_response = None;
    }

    fn refresh_blob_phase(&mut self) {
        if !self.blob_replication_is_negotiated() {
            self.blob_phase = BlobSyncPhase::Disabled;
            self.clear_pending_blob_exchange();
            return;
        }
        self.blob_phase = match self.blob_resume.as_ref() {
            None => BlobSyncPhase::Idle,
            Some(resume) if resume.manifest_complete => BlobSyncPhase::Complete,
            Some(resume) if resume.current_blob_id.is_some() => BlobSyncPhase::Chunk,
            Some(_) => BlobSyncPhase::Manifest,
        };
    }
}

fn same_branch_identity(left: &BranchHead, right: &BranchHead) -> bool {
    match (left.branch_id.as_deref(), right.branch_id.as_deref()) {
        (Some(left), Some(right)) => left == right,
        _ => left.branch_name == right.branch_name,
    }
}

/// Commit 包构建器。
///
/// 将 commit 列表打包成适合传输的批次。
pub struct BatchBuilder {
    max_per_batch: usize,
}

impl BatchBuilder {
    pub fn new(max_per_batch: usize) -> Self {
        Self {
            max_per_batch: max_per_batch.max(1),
        }
    }

    /// 将 commit 列表拆分为多个批次。
    pub fn build_batches(&self, commits: Vec<SerializedCommit>) -> Vec<CommitBatch> {
        let total = commits.len();
        let batch_count = total.div_ceil(self.max_per_batch);

        commits
            .chunks(self.max_per_batch)
            .enumerate()
            .map(|(i, chunk)| {
                CommitBatch::new(chunk.to_vec(), i as u32, i == batch_count.saturating_sub(1))
            })
            .collect()
    }
}

impl Default for BatchBuilder {
    fn default() -> Self {
        Self {
            max_per_batch: MAX_COMMITS_PER_BATCH,
        }
    }
}

